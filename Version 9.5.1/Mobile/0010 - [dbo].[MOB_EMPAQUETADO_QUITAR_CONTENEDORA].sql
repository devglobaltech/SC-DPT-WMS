IF  EXISTS (SELECT * FROM SYS.OBJECTS WHERE OBJECT_ID = OBJECT_ID(N'[DBO].[MOB_EMPAQUETADO_QUITAR_CONTENEDORA]') AND TYPE IN (N'P', N'PC'))
DROP PROCEDURE [DBO].[MOB_EMPAQUETADO_QUITAR_CONTENEDORA]

